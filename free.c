#include "so_long.h"

void    multy_free(char **f1, char **f2)
{
    int i;
    if(f1 && *f1)
    {
        i = 0;
        while(f1[i])
        {
            free(f1[i]);
            i++;
        }
        free(f1);
    }
    if(f2 && *f2)
    {
        i = 0;
        while(f2[i])
        {
            free(f2[i]);
            i++;
        }
        free(f2);
    }
}